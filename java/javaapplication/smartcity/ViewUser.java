    private void userTypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_userTypeActionPerformed
        // TODO add your handling code here:
        String selectedValue=userType.getSelectedItem().toString();
        Connection con=null;
        PreparedStatement pst=null;
        ResultSet rs=null;
        try
        {
            String url="jdbc:mysql://localhost:3306/SmartCity";
            String usern="root";
            String pwd="";
            con=DriverManager.getConnection(url,usern,pwd); 
            if(con!=null)
            {
                if(selectedValue.equals("Select"))
                {
                    jScrollPane2.setVisible(false);
                }
                else if(selectedValue.equals("Principal"))
                {
                    jScrollPane2.setVisible(true);
                    String query="select * from principal";
                    pst=con.prepareStatement(query);
                    rs=pst.executeQuery();
                    while(rs.next())
                    {
                        String id=String.valueOf(rs.getInt("s_no"));
                        String name=rs.getString("name");
                        String password=rs.getString("password");
                        String district=rs.getString("district");
                        String school=rs.getString("school");
                        String tbData[]={id,name,password,district,school};
                        DefaultTableModel tbModel=(DefaultTableModel)jTable1.getModel();
                        tbModel.addRow(tbData);
                    }
                }
                else if(selectedValue.equals("Contractor"))
                {
                    
                    ((DefaultTableModel)jTable1.getModel()).setRowCount(0);
                    String query="select * from contractor";
                    pst=con.prepareStatement(query);
                    rs=pst.executeQuery();
                    jTable1.getColumnModel().getColumn(4).setHeaderValue("Comapny Name");
                    while(rs.next())
                    {
                        String id=String.valueOf(rs.getInt("s_no"));
                        String name=rs.getString("name");
                        String password=rs.getString("password");
                        String district=rs.getString("district");
                        String school=rs.getString("company_name");
                        
                        String tbData[]={id,name,password,district,school};
                        DefaultTableModel tbModel=(DefaultTableModel)jTable1.getModel();
                        tbModel.addRow(tbData);
                    }
                }
                else if(selectedValue.equals("MLA"))
                {
                    String query="select * from mla";
                    pst=con.prepareStatement(query);
                    rs=pst.executeQuery();
                    jTable1.getColumnModel().getColumn(4).setHeaderValue("State");
                    while(rs.next())
                    {
                        String id=String.valueOf(rs.getInt("s_no"));
                        String name=rs.getString("name");
                        String password=rs.getString("password");
                        String district=rs.getString("district");
                        String school=rs.getString("state");
                        
                        String tbData[]={id,name,password,district,school};
                        DefaultTableModel tbModel=(DefaultTableModel)jTable1.getModel();
                        tbModel.addRow(tbData);
                    }
                }
                else if(selectedValue.equals("CM"))
                {
                    jScrollPane2.setVisible(true);
                    String query="select * from cm";
                    pst=con.prepareStatement(query);
                    rs=pst.executeQuery();
                    jTable1.getColumnModel().getColumn(3).setHeaderValue("State");
                    jTable1.getColumnModel().getColumn(4).setHeaderValue("null");
                    while(rs.next())
                    {
                        String id=String.valueOf(rs.getInt("s_no"));
                        String name=rs.getString("name");
                        String password=rs.getString("password");
                        String school=rs.getString("state");
                        
                        String tbData[]={id,name,password,school};
                        DefaultTableModel tbModel=(DefaultTableModel)jTable1.getModel();
                        tbModel.addRow(tbData);
                    }
                }
                
            }
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_userTypeActionPerformed

