    public void fillPrincipal()
    {
        Connection con=null;
        PreparedStatement pst=null;
        ResultSet rs=null;
        try
        {
            String url="jdbc:mysql://localhost:3306/SmartCity";
            String usern="root";
            String pwd="";
            con=DriverManager.getConnection(url,usern,pwd); 
            if(con!=null)
            {
                String query="select * from principal";
                    pst=con.prepareStatement(query);
                    rs=pst.executeQuery();
                    int id=1;
                    while(rs.next())
                    {
//                        String id=String.valueOf(rs.getInt("s_no"));
                        String sno=Integer.toString(id);
                        String name=rs.getString("name");
                        String password=rs.getString("password");
                        String district=rs.getString("district");
                        String school=rs.getString("school");
                        String tbData[]={sno,name,password,district,school};
                        DefaultTableModel tbModel=(DefaultTableModel)jTable2.getModel();
                        tbModel.addRow(tbData);
                        id++;
                    }
            }
        }
        catch(Exception ex)
        {
            ex.printStackTrace();
        }
    }
    
